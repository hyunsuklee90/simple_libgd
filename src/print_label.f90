    type :: t_font_container
        integer             ::  expand = 1 ! must be positive value greater than 1 
        integer             ::  nxt ! nx0 * expand + 2*dx
        integer             ::  nyt ! nx0 * expand + 2*dy
        
        integer             ::  nx ! nx0 * expand
        integer             ::  ny ! nx0 * expand
        integer             ::  nx0 = 9
        integer             ::  ny0 = 15
        integer             ::  nc = 255
        logical, pointer    ::  bmp(:,:,:)! => font_9_15	
        
        integer             ::  dx = 0 ! x gap 
        integer             ::  dy = 0 ! y gap 
    contains
    procedure   init => init_font_container
    procedure   get_mat_label
    procedure   iy => get_iy
    end type t_font_container
    
    type(t_font_container)  ::  font
    
    contains
    
    subroutine init_font_container(this, font0, expand)
    implicit none
    class(t_font_container)         ::  this
    integer, optional               ::  expand
    logical, target                 ::  font0(:,:,:)
    
    if (present(expand)) then
        this%expand = expand
    end if
    
    this%bmp => font0
    
    this%nc = size(font0,1)
    this%nx0 = size(font0,2)
    this%ny0 = size(font0,3)
    
    this%nx = this%nx0*this%expand
    this%ny = this%ny0*this%expand
    
    this%dy = 0
    this%dy = int(this%ny/12)+1
    
    this%nxt = this%nx+2*this%dx
    this%nyt = this%ny+2*this%dy
    
    end subroutine init_font_container
    
    subroutine get_mat_label(this, matname, matrgb, fmap)
    implicit none
    class(t_font_container)         ::  this
    character(*)                    ::  matname
    integer                         ::  matrgb(3)
    integer, allocatable            ::  fmap(:,:,:)
    
    logical, pointer                ::  bmp(:,:)
    integer                         ::  x, y
    integer                         ::  ii
    integer                         ::  nc, c, ix, iy
    integer                         ::  n_cbar = 5
    
    ! set background color
    fmap = 255
    
    ! Fill Material Color Label
    iy = this%dy    
    do c=1, n_cbar
        ix = (c-1)*this%nx
        do x=1, this%nx
            do y= 1, this%ny
                fmap(:,x+ix, y+iy) = matrgb
            end do
        end do
    end do
    
    
    nc = len(trim(matname))
    
    
    iy = this%dy
    do c=1, nc
        ix = (c-1)*this%nx + n_cbar*this%nx
        ii = ichar(matname(c:c))
        bmp => font%bmp(ii,:,:)
        do x=1, this%nx
            do y= 1, this%ny
                if (bmp(int((x-1)/this%expand)+1,int((y-1)/this%expand)+1)) then
                    fmap(:,x+ix,y+iy) = 0
                end if
            end do
        end do
    end do
    
    
    end subroutine get_mat_label
    
    function get_iy(this, i) result(iy)
    implicit none
    integer                         ::  i
    class(t_font_container)         ::  this
    integer                         ::  iy
    
    iy = (i-1)*this%ny + i*this%dy
    
    end function get_iy
    
    subroutine print_label()
    implicit none
    integer, allocatable            :: fmap(:,:,:)
    character(128)                  :: fname
    integer                         :: nc
    integer, allocatable            :: tmp_fmap(:,:,:)
    integer                         :: n_mat = 3
    character(len=128)              :: matnames(10)
    integer                         :: i, iy
    
    call font%init(font_9_15, 5)
    
    
    matnames(1) = " Fuel"
    matnames(2) = " Cool"
    matnames(3) = " ZrX"
    
    nc = 20
    
    allocate(fmap(1:3, 1:font%nxt*nc, 1:font%nyt*n_mat))
    
    allocate(tmp_fmap(1:3, font%nxt*nc, font%nyt))
    do i=1, n_mat
        iy = (i-1)*font%nyt
        call font%get_mat_label(trim(matnames(i)), [255, 100, 150], tmp_fmap)
        fmap(:,1:font%nxt*nc,iy+1:font%nyt*i) = tmp_fmap(:,1:font%nxt,:)
    end do
    
    fname = "test.gif"
    write(*,*) "run gdImageGif..."
    call gdImageGif(font%nxt*nc, font%nyt*n_mat, fmap, fname(1:1), len(trim(fname)))
    
    end subroutine print_label
    
